package mm.be.mathematic_function;

public class BasicFunctions {

    public int addTwoNumbers(int firstInputNumber, int secondInputNumber) {

        return firstInputNumber + secondInputNumber;
    }

    public int substractTwoNumbers(int firstInputNumber, int secondInputNumber) {

        return firstInputNumber - secondInputNumber;
    }

    public int multiplyTwoNumbers(int firstInputNumber, int secondInputNumber) {

        return firstInputNumber * secondInputNumber;
    }

    public double divideTwoNumbers(int firstInputNumber, int secondInputNumber) {

        return (double)firstInputNumber / secondInputNumber;
    }
}




